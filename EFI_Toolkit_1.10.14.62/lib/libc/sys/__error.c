#include <sys/cdefs.h>
#include <efi.h>
#define KERNEL
#include <errno.h>
#undef KERNEL

/*
 * Declare a weak reference in case the application is not linked
 * with libpthread.
 */
#if 0
#pragma weak __error=__error_unthreaded
#endif

int * __error_unthreaded()
{
	return(&errno);
}

int * __error()
{
    return __error_unthreaded();
}
